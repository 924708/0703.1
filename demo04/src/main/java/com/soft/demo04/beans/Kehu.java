package com.soft.demo04.beans;

//存储查询kehu表的数据

public class Kehu {

    private long kehuid;
    private String kehuname;
    private String kehutel;
    private long age;
    private String kehusex;
    private String city;
    private String identity;
    private long yonghuid;


    public long getKehuid() {
        return kehuid;
    }

    public void setKehuid(long kehuid) {
        this.kehuid = kehuid;
    }


    public String getKehuname() {
        return kehuname;
    }

    public void setKehuname(String kehuname) {
        this.kehuname = kehuname;
    }


    public String getKehutel() {
        return kehutel;
    }

    public void setKehutel(String kehutel) {
        this.kehutel = kehutel;
    }


    public long getAge() {
        return age;
    }

    public void setAge(long age) {
        this.age = age;
    }


    public String getKehusex() {
        return kehusex;
    }

    public void setKehusex(String kehusex) {
        this.kehusex = kehusex;
    }


    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }


    public String getIdentity() {
        return identity;
    }

    public void setIdentity(String identity) {
        this.identity = identity;
    }


    public long getYonghuid() {
        return yonghuid;
    }

    public void setYonghuid(long yonghuid) {
        this.yonghuid = yonghuid;
    }

}
